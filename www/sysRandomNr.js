//------------------------------------------------------------------------------
//- CONSTRUCTOR "sysRandomNr"
//------------------------------------------------------------------------------

function sysRandomNr() {
	this.number = '';
}


//------------------------------------------------------------------------------
//- METHOD "generate"
//------------------------------------------------------------------------------

sysRandomNr.prototype.generate = function(size) {

	for (var i=0; i<size; i++) {
		var tmpNr = Math.random()*10;
		this.number += Math.round(tmpNr);
	}

	return this.number;
}
